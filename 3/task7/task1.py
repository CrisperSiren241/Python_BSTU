# Поиск дат:
# Создайте строку text, содержащую произвольный текст, в котором присутствуют даты в формате "дд.мм.гггг".
# Используя модуль re, выполните поиск всех дат в тексте с помощью регулярного выражения.
# Выведите на экран все найденные даты.

import re

# Произвольный текст с датами
text = """
Сегодняшняя дата: 14.03.2024.
Вчерашняя дата была: 13.03.2024.
Дата завтра: 15.03.2024.
Также есть еще даты в других форматах, например 12.03.24 или 1.3.2024.
"""

# Регулярное выражение для поиска дат в формате "дд.мм.гггг"
date_pattern = r"\d{1,2}\.\d{1,2}\.\d{4}"

# Поиск всех дат в тексте
found_dates = re.findall(date_pattern, text)

# Вывод найденных дат на экран
print("Найденные даты:")
for date in found_dates:
    print(date)
