# Напишите программу: 
# а) для распаковки кортежа в несколько переменных; 
# б) для преобразования списка кортежей в словарь.


# а) Распаковка кортежа в несколько переменных
x, y, z = (10, 20, 30)

# б) Преобразование списка кортежей в словарь
tuple_list = [("a", 1), ("b", 2), ("c", 3)]
dict_from_tuples = dict(tuple_list)

print("Распакованные переменные из кортежа:", x, y, z)
print("Словарь из списка кортежей:", dict_from_tuples)
