# Используйте модуль random. Компьютер загадывает число от А до В (вводятся с клавиатуры). 
# Напишите угадайку этого числа. 
# Программа должна просит игрока угадать число. После того как игрок ввел число, программа должна выводит сообщение «Твоё число больше, чем нужно!» или «Твоё число меньше, чем нужно!». И заново просить угадать число. Когда игрок угадал вывести слово «Победа!»


import random
0
A = int(input("Введите нижнюю границу загадываемого числа: "))
B = int(input("Введите верхнюю границу загадываемого числа: "))

if B <= A:
    print("Ошибка: верхняя граница должна быть больше нижней.")
    exit()

ran_number = random.randint(A, B)

print("Компьютер загадал число от", A, "до", B)

while True:
    guess = int(input("Угадайте число: "))

    if guess < ran_number:
        print("Твоё число меньше, чем нужно!")
    elif guess > ran_number:
        print("Твоё число больше, чем нужно!")
    else:
        print("Победа! Компьютер загадал число", ran_number)
        break
